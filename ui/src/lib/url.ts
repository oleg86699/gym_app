/**
 * Человекочитаемый вид URL для ОТОБРАЖЕНИЯ.
 *
 * WordPress возвращает permalink с percent-encoded slug: кириллица (и любой
 * другой не-ASCII) превращается в `%d1%80%d0%b5...`. `decodeURI` раскодирует
 * UTF-8 обратно в буквы ЛЮБОГО языка, но НЕ трогает структурные символы URL
 * (`/ ? & # = :` и пр.) — поэтому ссылка остаётся валидной, просто читаемой.
 *
 * Применяем только как ТЕКСТ ссылки; `href` оставляем сырым (encoded) — это
 * канонический вид, который гарантированно кликается и копируется везде.
 */
export function prettyUrl(url: string | null | undefined): string {
  if (!url) return ''
  try {
    return decodeURI(url)
  } catch {
    // Битая %-последовательность / невалидный UTF-8 — показываем как есть.
    return url
  }
}
